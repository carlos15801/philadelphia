    public void cargar(){
        List<Oficial> oficiales =  OficialDAO.obtenerTodos();
        DefaultTableModel modeloTabla = (DefaultTableModel)tblOficial.getModel();
        modeloTabla.setRowCount(0);
        for(Oficial o : oficiales){
            modeloTabla.addRow(new Object[] {
                o.getId(),
                o.getNombre(),
                o.getTelefono(),
            });
        }
    }
    
    
    
    
